#!/usr/bin/env python3
"""Auto-generates production-grade structured logging configuration.

Detects the project language/framework and generates optimal structlog (Python)
or pino (Node.js) configuration with correlation IDs, exception formatting,
callsite info, and OpenTelemetry trace correlation.

Usage:
    python3 structlog_configurator.py detect <project_dir>
    python3 structlog_configurator.py generate <project_dir> [--framework flask|fastapi|django|express|nextjs]
    python3 structlog_configurator.py generate src/ --python --output logging_config.py
    python3 structlog_configurator.py generate src/ --node --output logger.ts
"""

import sys
import json
import argparse
from pathlib import Path
from typing import Dict, List, Optional, Tuple

PYTHON_STRUCTLOG_CONFIG = '''"""
Auto-generated structured logging configuration.
Generated by enterprise-deep-debug/structlog_configurator.py

Features:
- JSON output in production, colored console in development
- Automatic correlation ID propagation via contextvars
- Exception traceback as structured dict
- Callsite info (file, function, line)
- OpenTelemetry trace/span ID injection (if otel available)
- Timestamp in ISO 8601 format
"""
import logging
import sys
import os
import structlog
from structlog.contextvars import merge_contextvars, bind_contextvars, clear_contextvars

IS_PRODUCTION = os.getenv("ENVIRONMENT", "development").lower() in ("production", "prod", "staging")


def setup_logging(level: str = "DEBUG", json_output: bool = None):
    """Call this once at application startup."""
    if json_output is None:
        json_output = IS_PRODUCTION

    shared_processors = [
        merge_contextvars,
        structlog.contextvars.merge_contextvars,
        structlog.stdlib.add_log_level,
        structlog.stdlib.add_logger_name,
        structlog.processors.TimeStamper(fmt="iso"),
        structlog.processors.StackInfoRenderer(),
        structlog.processors.UnicodeDecoder(),
        structlog.processors.CallsiteParameterAdder(
            parameters=[
                structlog.processors.CallsiteParameter.FILENAME,
                structlog.processors.CallsiteParameter.FUNC_NAME,
                structlog.processors.CallsiteParameter.LINENO,
            ]
        ),
    ]

    # OpenTelemetry trace correlation (optional)
    try:
        from opentelemetry import trace

        def add_otel_context(logger, method_name, event_dict):
            span = trace.get_current_span()
            if span and span.is_recording():
                ctx = span.get_span_context()
                event_dict["trace_id"] = format(ctx.trace_id, "032x")
                event_dict["span_id"] = format(ctx.span_id, "016x")
            return event_dict

        shared_processors.append(add_otel_context)
    except ImportError:
        pass

    if json_output:
        renderer = structlog.processors.JSONRenderer()
    else:
        renderer = structlog.dev.ConsoleRenderer(
            colors=sys.stderr.isatty(),
            exception_formatter=structlog.dev.plain_traceback,
        )

    structlog.configure(
        processors=[
            *shared_processors,
            structlog.processors.format_exc_info,
            structlog.processors.EventRenamer("msg"),
            renderer,
        ],
        wrapper_class=structlog.stdlib.BoundLogger,
        context_class=dict,
        logger_factory=structlog.stdlib.LoggerFactory(),
        cache_logger_on_first_use=True,
    )

    # Also configure stdlib logging to go through structlog
    formatter = structlog.stdlib.ProcessorFormatter(
        processors=[
            structlog.stdlib.ProcessorFormatter.remove_processors_meta,
            *shared_processors,
            structlog.processors.format_exc_info,
            renderer,
        ],
    )

    handler = logging.StreamHandler(sys.stderr)
    handler.setFormatter(formatter)

    root = logging.getLogger()
    root.handlers.clear()
    root.addHandler(handler)
    root.setLevel(getattr(logging, level.upper(), logging.DEBUG))


def get_logger(name: str = None):
    """Get a structlog logger with the given name."""
    return structlog.get_logger(name)


# Auto-setup if imported
setup_logging()
'''

PYTHON_FASTAPI_MIDDLEWARE = '''
"""Request logging middleware for FastAPI with correlation ID propagation."""
import uuid
import time
import structlog
from starlette.middleware.base import BaseHTTPMiddleware
from starlette.requests import Request
from structlog.contextvars import bind_contextvars, clear_contextvars

logger = structlog.get_logger(__name__)


class RequestLoggingMiddleware(BaseHTTPMiddleware):
    async def dispatch(self, request: Request, call_next):
        clear_contextvars()

        request_id = request.headers.get("X-Request-ID", str(uuid.uuid4())[:8])
        bind_contextvars(
            request_id=request_id,
            method=request.method,
            path=str(request.url.path),
            client=request.client.host if request.client else "unknown",
        )

        start = time.perf_counter()
        logger.info("request_started")

        try:
            response = await call_next(request)
            elapsed = (time.perf_counter() - start) * 1000
            logger.info("request_completed",
                        status=response.status_code,
                        elapsed_ms=round(elapsed, 2))
            response.headers["X-Request-ID"] = request_id
            return response
        except Exception as exc:
            elapsed = (time.perf_counter() - start) * 1000
            logger.error("request_failed",
                         error=str(exc),
                         error_type=type(exc).__name__,
                         elapsed_ms=round(elapsed, 2),
                         exc_info=True)
            raise
'''

