def detect_project(project_dir: Path) -> Dict:
    info = {
        "languages": [],
        "frameworks": [],
        "has_logging": False,
        "recommendations": [],
    }

    if list(project_dir.rglob("*.py")):
        info["languages"].append("python")
    if list(project_dir.rglob("*.ts")) or list(project_dir.rglob("*.tsx")):
        info["languages"].append("typescript")
    if list(project_dir.rglob("*.js")) or list(project_dir.rglob("*.jsx")):
        info["languages"].append("javascript")

    pkg_json = project_dir / "package.json"
    if pkg_json.exists():
        try:
            pkg = json.loads(pkg_json.read_text())
            deps = {**pkg.get("dependencies", {}), **pkg.get("devDependencies", {})}
            if "next" in deps:
                info["frameworks"].append("nextjs")
            if "express" in deps:
                info["frameworks"].append("express")
            if "fastify" in deps:
                info["frameworks"].append("fastify")
            if "pino" in deps:
                info["has_logging"] = True
            if "winston" in deps:
                info["has_logging"] = True
        except Exception:
            pass

    req_txt = project_dir / "requirements.txt"
    pyproject = project_dir / "pyproject.toml"
    for dep_file in [req_txt, pyproject]:
        if dep_file.exists():
            content = dep_file.read_text().lower()
            if "fastapi" in content:
                info["frameworks"].append("fastapi")
            if "flask" in content:
                info["frameworks"].append("flask")
            if "django" in content:
                info["frameworks"].append("django")
            if "structlog" in content:
                info["has_logging"] = True

    if "python" in info["languages"] and not info["has_logging"]:
        info["recommendations"].append("Add structlog for structured Python logging")
    if (
        "typescript" in info["languages"] or "javascript" in info["languages"]
    ) and not info["has_logging"]:
        info["recommendations"].append("Add pino for structured Node.js logging")

    return info


def generate_config(
    project_dir: Path, language: str, framework: str = None, output: str = None
) -> str:
    if language == "python":
        config = PYTHON_STRUCTLOG_CONFIG
        if framework in ("fastapi", "flask"):
            config += "\n\n" + PYTHON_FASTAPI_MIDDLEWARE
        out_path = output or "logging_config.py"
    elif language in ("typescript", "javascript", "node"):
        config = NODE_PINO_CONFIG
        out_path = output or "logger.ts"
    else:
        print(f"Unsupported language: {language}", file=sys.stderr)
        sys.exit(1)

    if output:
        out_file = project_dir / out_path
        out_file.write_text(config)
        print(f"Generated {out_file}", file=sys.stderr)

    return config


def main():
    parser = argparse.ArgumentParser(
        description="Auto-generate structured logging configuration"
    )
    subparsers = parser.add_subparsers(dest="command")

    detect_p = subparsers.add_parser("detect", help="Detect project type")
    detect_p.add_argument("project_dir")

    gen_p = subparsers.add_parser("generate", help="Generate logging config")
    gen_p.add_argument("project_dir")
    gen_p.add_argument("--python", action="store_true")
    gen_p.add_argument("--node", action="store_true")
    gen_p.add_argument(
        "--framework",
        choices=["flask", "fastapi", "django", "express", "nextjs", "fastify"],
    )
    gen_p.add_argument("--output", help="Output filename")

    args = parser.parse_args()

    if args.command == "detect":
        info = detect_project(Path(args.project_dir))
        print(json.dumps(info, indent=2))
    elif args.command == "generate":
        lang = "python" if args.python else "node" if args.node else None
        if not lang:
            info = detect_project(Path(args.project_dir))
            lang = info["languages"][0] if info["languages"] else "python"
        config = generate_config(
            Path(args.project_dir), lang, args.framework, args.output
        )
        if not args.output:
            print(config)
    else:
        parser.print_help()


if __name__ == "__main__":
    main()
